module constructiveheuristcs

  use instance, only: instance_type
  use solution, only: solution_type
  use statistics, only: statistics_type
  use dijkstra, only: DijkstraRoute
  use blockcoordinatedescent, only: BCD
  
  implicit none

  logical, parameter :: debug = .false.

  ! EXTERNAL FUNCTIONS
  real(kind=8), external :: drand 
  
  public :: CW,NN
  
contains
  
  ! *****************************************************************
  ! CLARKE & WRIGHT CONSTRUCTIVE HEURISTIC FOR VRP
  ! *****************************************************************

  subroutine CW(stat,inst,sol,seed,ptype,prand)

    ! This routine constructs an initial feasible solution for the
    ! GVRP and VRPN using the Clarke and Wright savings heuristic.
    ! Initial routes are generated by assigning polygon centroids as
    ! provisional visit points. Subsequently, a refinement procedure
    ! determines the precise visit location within each polygon.  For
    ! GVRP (ptype = 'G'), the lowest-cost route is computed using
    ! Dijkstra's algorithm, and visit points are set as polygon
    ! vertices along the route. For VRPN (ptype = 'N'), visit points
    ! are optimized using a Block Coordinate Descent (BCD) method,
    ! allowing any position along the polygon boundaries.

    implicit none

    ! SCALAR ARGUMENTS
    character, intent(in) :: ptype
    real(kind=8), intent(inout) :: seed
    real(kind=8), intent(in) :: prand
    type(instance_type), intent(in) :: inst
    type(solution_type), intent(inout) :: sol
    type(statistics_type), intent(inout) :: stat
      
    ! LOCAL SCALARS
    integer :: i,j,k,next_i,ridA,ridB,rncA,rncB,rnc
    real(kind=8) :: area,cf,sumx,sumy
        
    ! LOCAL ARRAYS
    integer :: clientA(inst%nclients*(inst%nclients-1)/2),clientB(inst%nclients*(inst%nclients-1)/2)
    real(kind=8) :: saving(inst%nclients*(inst%nclients-1)/2),c(2,0:inst%nclients),dist(0:inst%nclients,0:inst%nclients)
   
    !Barycenter calculation
    c(1:2,0) = inst%p(1:2,1,0)
    do k = 1,inst%nclients
       if ( inst%np(k) .eq. 1 ) then
          ! Single-point polygon: the barycenter coincides with the point
          c(1:2,k) = inst%p(1:2,1,k)
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)
          
       else if ( inst%np(k) .eq. 2 ) then
          ! Two-point polygon: the barycenter is the midpoint
          c(1:2,k) = 0.5d0 * ( inst%p(1:2,1,k) + inst%p(1:2,2,k) )
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)
          
       else !if ( inst%np(k) .ge. 3 ) then
          area = 0.0d0
          sumx = 0.0d0
          sumy = 0.0d0
          
          do i = 1,inst%np(k)
             next_i = mod( i,inst%np(k) ) + 1          
             
             cf = inst%p(1,i,k)*inst%p(2,next_i,k) - inst%p(1,next_i,k)*inst%p(2,i,k)
             
             area = area + cf
             sumx = sumx + (inst%p(1,i,k) + inst%p(1,next_i,k)) * cf
             sumy = sumy + (inst%p(2,i,k) + inst%p(2,next_i,k)) * cf
          end do
          
          area = area * 0.5d0
          c(1:2,k) = (/ sumx, sumy /) / (6.0d0 * area)
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)
       end if
    end do
       
    ! Matrix of distance for centroids/barycenter
    do i = 0,inst%nclients
       do j = i + 1,inst%nclients
          dist(i,j) = norm2( c(1:2,i) - c(1:2,j) )
          dist(j,i) = dist(i,j)
       end do
       dist(i,i) = 0.0d0
    end do
    
    ! Savings calculation
    k = 0
    do i = 1,inst%nclients - 1
       do j = i + 1,inst%nclients
          k = k + 1
          saving(k) = dist(0,i) + dist(0,j) - dist(i,j)
          clientA(k) = i
          clientB(k) = j
          if ( debug ) write(*, *) 'saving (', i, ',', j, ') = ',saving(k)
       end do
    end do

    ! Sorting savings
    do i = 1,k
       do j = i + 1,k
          if ( saving(i) .lt. saving(j) ) then
             call dswap(saving(i),saving(j))
             call iswap(clientA(i),clientA(j))
             call iswap(clientB(i),clientB(j))
          end if
       end do
       if ( debug ) write(*, *) 'saving = ',saving(i),' first client = ',clientA(i),' second client = ',clientB(i)
    end do

    ! Add a randomization to the sort
    do i = 1,k - 1
       if ( drand( seed ) .le. prand ) then
          j = i + 1 + floor( ( k - i ) * drand( seed ) )
          call dswap(saving(i),saving(j))
          call iswap(clientA(i),clientA(j))
          call iswap(clientB(i),clientB(j))          
       end if
       if ( debug ) write(*, *) 'saving2 = ',saving(i),' first client = ',clientA(i),' second client = ',clientB(i)
    end do

    ! We start with individual routes for each client
    sol%nroutes = inst%nclients ! there are as many routes as clients
    sol%nclients(1:sol%nroutes) = 1 ! each route has a single client
    sol%route(1,1:sol%nroutes) = (/ (i,i=1,inst%nclients) /) ! route i corresponds to depot, client i, depot, for i=1,nroutes
    sol%length(1:sol%nroutes) = 2.0d0 * dist(0,1:sol%nroutes) ! the length of the route is twice the distance from the depot to the client    
    sol%aggdem(1:sol%nroutes) = inst%dem(1:sol%nroutes) ! the aggregated demand of the route is equal to the demand of the client
    sol%clientroute(1:inst%nclients) = (/ (i,i=1,inst%nclients) /) ! each client i is assignment to route i, for i=1,nclients

    ! Main loop
    do k = 1,inst%nclients * ( inst%nclients - 1 ) / 2
     
       if ( saving(k) .gt. 0.0d0 .and. sol%clientroute(clientB(k)) .ne. sol%clientroute(clientA(k)) .and. &
            sol%length(sol%clientroute(clientA(k))) + sol%length(sol%clientroute(clientB(k))) - saving(k) .le. inst%maxlen .and. &
            sol%aggdem(sol%clientroute(clientA(k))) + sol%aggdem(sol%clientroute(clientB(k))) .le. inst%maxcap ) then
          ! ------------------------------------------------------------
          ! When clients A and B belong to different routes, there are
          ! four cases in which routes can be joined: (a) A is the first
          ! and B is the last on their own routes; (b) B is the first
          ! and A is the last on their own routes; (c) A and B are both
          ! the first in their own routes; and (d) A and B are both the
          ! last in their own routes.
          ! ------------------------------------------------------------

          ridA = sol%clientroute(clientA(k))
          ridB = sol%clientroute(clientB(k))
          rncA = sol%nclients(ridA)
          rncB = sol%nclients(ridB)

          ! case (a): in this case we concatenate the route of A at the end of the route of B
          if ( sol%route(1,ridA) .eq. clientA(k) .and. sol%route(rncB,ridB) .eq. clientB(k) ) then
             sol%route(rncB+1:rncB+rncA,ridB) = sol%route(1:rncA,ridA) ! Add clients of A's route at the end of B's route
             sol%clientroute(sol%route(1:rncA,ridA)) = ridB ! Redefine the route to which the clients being moved belong
             sol%nclients(ridB) = sol%nclients(ridB) + sol%nclients(ridA) ! Set the number of client of the new route
             sol%length(ridB) = sol%length(ridB) + sol%length(ridA) - saving(k) ! Set the length of the new route
             sol%aggdem(ridB) = sol%aggdem(ridB) + sol%aggdem(ridA) ! Set the aggregated demmand of the new route
             sol%nclients(ridA) = 0 ! Set to zero the number of client of the route that was extinct
             sol%length(ridA) = 0.0d0 ! Set to zero the length of the route that was extinct
             sol%aggdem(ridA) = 0.0d0 ! Set to zero the aggregated demmand of the route that was extinct

          ! case (b): in this case we concatenate the route of B at the end of the route of A
          else if ( sol%route(1,ridB) .eq. clientB(k) .and. sol%route(rncA,ridA) .eq. clientA(k) ) then
             sol%route(rncA+1:rncA+rncB,ridA) = sol%route(1:rncB,ridB) ! Add clients of B's route at the end of A's route
             sol%clientroute(sol%route(1:rncB,ridB)) = ridA ! Redefine the route to which the clients being moved belong
             sol%nclients(ridA) = sol%nclients(ridA) + sol%nclients(ridB) ! Set the length of the new route
             sol%length(ridA) = sol%length(ridA) + sol%length(ridB) - saving(k) ! Set the length of the new route
             sol%aggdem(ridA) = sol%aggdem(ridA) + sol%aggdem(ridB) ! Set the aggregated demmand of the new route
             sol%nclients(ridB) = 0 ! Set to zero the number of client of the route that was extinct
             sol%length(ridB) = 0.0d0 ! Set to zero the length of the route that was extinct
             sol%aggdem(ridB) = 0.0d0 ! Set to zero the aggregated demmand of the route that was extinct

          ! case (c): in this case we reverse the route of B (for B to be the last of its route, and apply case (a))
          else if ( sol%route(1,ridA) .eq. clientA(k) .and. sol%route(1,ridB) .eq. clientB(k) ) then
             sol%route(1:rncB,ridB) = sol%route(rncB:1:-1,ridB) ! Reverse B's route
             sol%route(rncB+1:rncB+rncA,ridB) = sol%route(1:rncA,ridA) ! Add clients of A's route at the end of B's route
             sol%clientroute(sol%route(1:rncA,ridA)) = ridB ! Redefine the route to which the clients being moved belong
             sol%nclients(ridB) = sol%nclients(ridB) + sol%nclients(ridA) ! Set the length of the new route
             sol%length(ridB) = sol%length(ridB) + sol%length(ridA) - saving(k) ! Set the length of the new route
             sol%aggdem(ridB) = sol%aggdem(ridB) + sol%aggdem(ridA) ! Set the aggregated demmand of the new route
             sol%nclients(ridA) = 0 ! Set to zero the number of client of the route that was extinct
             sol%length(ridA) = 0.0d0 ! Set to zero the length of the route that was extinct
             sol%aggdem(ridA) = 0.0d0 ! Set to zero the aggregated demmand of the route that was extinct

          ! case (d): in this case we reverse the route of A (for A to be the first of its route, and apply case (a))
          else if ( sol%route(rncA,ridA) .eq. clientA(k) .and. sol%route(rncB,ridB) .eq. clientB(k) ) then
             sol%route(1:rncA,ridA) = sol%route(rncA:1:-1,ridA) ! Reverse A's route
             sol%route(rncB+1:rncB+rncA,ridB) = sol%route(1:rncA,ridA) ! Add clients of A's route at the end of B's route
             sol%clientroute(sol%route(1:rncA,ridA)) = ridB ! Redefine the route to which the clients being moved belong
             sol%nclients(ridB) = sol%nclients(ridB) + sol%nclients(ridA) ! Set the number of client the new route
             sol%length(ridB) = sol%length(ridB) + sol%length(ridA) - saving(k) ! Set the length of the new route
             sol%aggdem(ridB) = sol%aggdem(ridB) + sol%aggdem(ridA) ! Set the aggregated demmand of the new route
             sol%nclients(ridA) = 0 ! Set to zero the number of client of the route that was extinct
             sol%length(ridA) = 0.0d0 ! Set to zero the length of the route that was extinct
             sol%aggdem(ridA) = 0.0d0 ! Set to zero the aggregated demmand of the route that was extinct
          end if
       end if
    end do

    ! Determines the visit point for each client in the GVRP or CVRPN.
    do k = 1,sol%nroutes
       rnc = sol%nclients(k)
       if( rnc .gt. 0 ) then
          if ( ptype .eq. 'G' ) then
             call DijkstraRoute(inst,rnc,sol%route(1:rnc,k),sol%pathind(1:rnc,k),sol%length(k))
             do j = 1,rnc
                sol%pathcoo(1:2,j,k) = inst%p(1:2,sol%pathind(j,k),sol%route(j,k))
             end do
          else ! if ( ptype .eq. 'N' ) then
             do j = 1,rnc
                sol%pathcoo(1:2,j,k) = c(1:2,sol%route(j,k))
                sol%pathind(j,k) = -1
             end do
             call BCD(stat,inst,rnc,sol%route(1:rnc,k),sol%pathcoo(1:2,1:rnc,k),sol%length(k))
          end if
       end if
    end do
    
  end subroutine CW
  
  ! *****************************************************************
  ! NEAREST NEIGHBOR CONSTRUCTIVE HEURISTIC FOR VRP
  ! *****************************************************************

  subroutine NN(stat,inst,sol,seed,ptype,nbestmin,pbest)

    ! This routine constructs an initial feasible solution for the
    ! GVRP and VRPN using a nearest neighbor constructive heuristic.
    ! Initial routes are generated by assigning polygon centroids as
    ! provisional visit points. Subsequently, a refinement procedure
    ! determines the precise visit location within each polygon.  For
    ! GVRP (ptype = 'G'), the lowest-cost route is computed using
    ! Dijkstra's algorithm, and visit points are set as polygon
    ! vertices along the route. For VRPN (ptype = 'N'), visit points
    ! are optimized using a Block Coordinate Descent (BCD) method,
    ! allowing any position along the polygon boundaries.
    
    implicit none
    
    ! SCALAR ARGUMENTS
    character, intent(in) :: ptype
    integer, intent(in) :: nbestmin
    real(kind=8), intent(in) :: pbest
    real(kind=8), intent(inout) :: seed
    type(instance_type), intent(in) :: inst
    type(solution_type), intent(inout) :: sol
    type(statistics_type), intent(inout) :: stat

    ! PARAMETERS
    !integer, parameter :: nbestmin = 3 ! nbestmin = 1 for NO random constructions
    !real(kind=8), parameter :: pbest = 0.2d0 ! pbest = 0.0d0 for NO random constructions
    
    ! LOCAL SCALARS
    integer :: i,j,k,r,s,current,nbest,next_i,nextclient,nleft,rnc
    real(kind=8) :: area,cf,sumx,sumy
    
    ! LOCAL ARRAYS
    integer :: candidates(inst%nclients)
    real(kind=8) :: c(2,0:inst%nclients),dist(0:inst%nclients,0:inst%nclients),distances(inst%nclients)
    
    !Barycenter calculation
    c(1:2,0) = inst%p(1:2,1,0)
    do k = 1,inst%nclients
       if ( inst%np(k) .eq. 1 ) then
          ! Single-point polygon: the barycenter coincides with the point
          c(1:2,k) = inst%p(1:2,1,k)
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)

       else if ( inst%np(k) .eq. 2 ) then
          ! Two-point polygon: the barycenter is the midpoint
          c(1:2,k) = 0.5d0 * ( inst%p(1:2,1,k) + inst%p(1:2,2,k) )
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)

       else !if ( inst%np(k) .ge. 3 ) then
          area = 0.0d0
          sumx = 0.0d0
          sumy = 0.0d0

          do i = 1,inst%np(k)
             next_i = mod( i,inst%np(k) ) + 1          

             cf = inst%p(1,i,k)*inst%p(2,next_i,k) - inst%p(1,next_i,k)*inst%p(2,i,k)

             area = area + cf
             sumx = sumx + (inst%p(1,i,k) + inst%p(1,next_i,k)) * cf
             sumy = sumy + (inst%p(2,i,k) + inst%p(2,next_i,k)) * cf
          end do

          area = area * 0.5d0
          c(1:2,k) = (/ sumx, sumy /) / (6.0d0 * area)
          if( debug ) write(*,*) 'barycenter of client ',k,'=',c(1:2,k)
       end if
    end do
    
           
    ! Matrix of distance for centroids/barycenter
    do i = 0,inst%nclients
       do j = i + 1,inst%nclients
          dist(i,j) = norm2( c(1:2,i) - c(1:2,j) )
          dist(j,i) = dist(i,j)
       end do
       dist(i,i) = 0.0d0
    end do

    ! Minimum feasibility check: each client must be visitable within a single route
    do i = 1,inst%nclients
       if ( 2.0d0 * dist(0,i) .gt. inst%maxlen ) then
          write(*,*) 'ERROR: Client ',i,' cannot be served individually (route length exceeds maxlen).'
          write(*,*) 'Route length = ',2.0d0 * dist(0, i),' maxlen = ',inst%maxlen
          stop
       end if
    end do

    ! Initialize solution for Nearest Neighbor algorithm
    sol%nroutes = inst%nclients            ! there are as many routes as clients
    sol%nclients(1:sol%nroutes) = 0        ! each route initially serves zero clients
    sol%length(1:sol%nroutes) = 0.0d0      ! each route initially has zero total length
    sol%aggdem(1:sol%nroutes) = 0.0d0      ! each route initially has zero aggregated demand
    sol%clientroute(1:inst%nclients) = - 1 ! each client is initially unassigned

    nleft = inst%nclients

    r = 1
    current = 0

    ! Building routes
    do while ( nleft .gt. 0 )
       ! Classify unvisited clients by their distance to the current client
       
       candidates(1:nleft) = pack( (/ (k, k=1,inst%nclients) /), sol%clientroute(1:inst%nclients) .eq. - 1 )
       distances(1:nleft) = dist(current,candidates(1:nleft))
              
       do j = 1,nleft - 1
          do k = j + 1,nleft
             if ( distances(k) .lt. distances(j) ) then
                call dswap(distances(j),distances(k))
                call iswap(candidates(j),candidates(k))
             end if
          end do
       end do
          
       ! Select next client

       nbest = min( max( nbestmin, 1 + floor( pbest * ( nleft - 1 ) ) ), nleft )
       s = min( 1 + floor( drand(seed) * nbest ), nbest )
       nextclient = candidates(s)
                        
       ! Add client to a route
       
       if ( sol%length(r) + dist(current,nextclient) + dist(nextclient,0) .le. inst%maxlen .and. &
            sol%aggdem(r) + inst%dem(nextclient) .le. inst%maxcap ) then
          ! If feasible, add it to the open rote
          sol%nclients(r) = sol%nclients(r) + 1
          sol%route(sol%nclients(r),r) = nextclient
          sol%length(r) = sol%length(r) + dist(current,nextclient)
          sol%aggdem(r) = sol%aggdem(r) + inst%dem(nextclient)
          sol%clientroute(nextclient) = r
          nleft = nleft - 1

          current = nextclient
          
          if ( nleft .eq. 0 ) then
             ! Close the route by returning to the depot
             sol%length(r) = sol%length(r) + dist(current,0)
          end if
                    
          if ( debug ) write(*,*) 'Client added: ',nextclient,' remaining clients = ',nleft
          
       else
          ! Close the route by returning to the depot
          sol%length(r) = sol%length(r) + dist(current,0)
          
          if ( debug ) write(*,*) 'Route ',r,' completed. Total route length: ',sol%length(r)

          ! Open a new route
          r = r + 1     
          current = 0

          if ( debug ) write(*,*) 'Building route number: ',r
       end if
    end do
                
    ! Determines the visit point for each client in the GVRP or CVRPN.
    do k = 1,sol%nroutes
       rnc = sol%nclients(k)
       if ( rnc .gt. 0 ) then
          if ( ptype .eq. 'G' ) then
             call DijkstraRoute(inst,rnc,sol%route(1:rnc,k),sol%pathind(1:rnc,k),sol%length(k))
             do j = 1,rnc
                sol%pathcoo(1:2,j,k) = inst%p(1:2,sol%pathind(j,k),sol%route(j,k))
             end do
          else ! if ( ptype .eq. 'N' ) then
             do j = 1,rnc
                sol%pathcoo(1:2,j,k) = c(1:2,sol%route(j,k))
                sol%pathind(j,k) = -1
             end do
             call BCD(stat,inst,rnc,sol%route(1:rnc,k),sol%pathcoo(1:2,1:rnc,k),sol%length(k))
          end if
       end if
    end do
    
  end subroutine NN
  
  ! *****************************************************************
  ! *****************************************************************

  subroutine iswap(a,b)
    
    implicit none
    
    ! SCALAR ARGUMENTS
    integer, intent(inout) :: a,b
    
    ! LOCAL SCALARS
    integer :: temp
    
    temp = a
    a = b
    b = temp
    
  end subroutine iswap
  
  subroutine dswap(a,b)
    
    implicit none
    
    ! SCALAR ARGUMENTS
    real(kind=8), intent(inout) :: a,b
    
    ! LOCAL SCALARS
    real(kind=8) :: temp
    
    temp = a
    a = b
    b = temp
    
  end subroutine dswap

end module constructiveheuristcs
